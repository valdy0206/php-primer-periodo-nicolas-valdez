                    <h3><span>1</span>Concepto</h3>
                    <p>PHP es un lenguaje de programación que se usa para crear páginas web dinámicas. Se ejecuta en el lado del servidor y se puede incrustar en HTML.</p>
                    <br>
                    <h3><span>2</span>Características</h3>
                    <p>1. GRAN EXTENSIÓN DE DOCUMENTACIÓN
                        <br><br>
                        Ya sea creada de manera oficial como por los usuarios, PHP tiene la ventaja de ser usado por millones de usuarios en muchos proyectos. De cara al nuevo programador es una gran ventaja porqué no hay nada mejor que un lenguaje con una gran comunidad detrás, sobre todo para los no tan afines al idioma anglosajón.
                        <br><br><br>
                        2. VARIEDAD DE HERRAMIENTAS PARA APRENDER
                        <br><br>
                        Tenemos varios métodos para iniciarnos: podemos contratar un servidor remoto para subir los archivos, como haríamos con cualquier web, lo cual tiene lógica ya que si se tiene el sitio en la red ya se tendría este recurso; podemos crear un servidor local con una máquina virtual, o lo más popular entre los alumnos: descargar la herramienta libre WAMP (XAMP o LAMP en función de si es para Mac OSX o Linux). Esta herramienta simula que vuestra máquina local es un servidor web para poder ejecutar los ficheros PHP. Es decir, sea cual sea la situación, es imposible que se pueda quedar fuera, sobretodo teniendo en cuenta que no requiere un entorno de desarrollo complejo, como ocurre con Titanium.
                        <br><br><br>
                        3. MUCHA OFERTA DE TRABAJO
                        <br><br>
                        Hoy en día las webs más complejas funcionan bajo PHP, por lo que son muchas las empresas que requieren los servicios de un desarrollador de este lenguaje.
                        <br><br><br>
                        4. PERMITE PROGRAMACIÓN ORIENTADA A OBJETOS 
                        <br><br>
                        Esto significa que se pueden dividir los scripts en métodos, clases, etc. para hacer más ágil de cara al servidor el procesamiento de los datos.
                        <br><br><br>
                        5. MÓDULOS EXTERNOS PARA MEJORAR LA APLICACIÓN WEB
                        <br><br>
                        Gracias a esto, se puede implementar funcionalidades que de por sí no existen, siendo una gran solución a las necesidades. Por ejemplo, en vez de programar de cero una pantalla de registro, se puede usar un módulo ya hecho. Esto no sería posible si no fuese orientado a objetos, como menciono en el punto anterior.
                        <br><br><br>
                        6. SE PUEDE SEPARAR LA ESTRUCTURA 
                        <br><br>
                        Esto quiere decir que se puede tener de manera independiente el código que se encarga de mover los datos del que se enlaza a la interfaz. Esto se conoce como Modelo Vista/Controlador (MVC). Gracias a esta característica, no “contaminaremos” código con líneas innecesarias y lo mantendremos limpio y ordenado.
                        <br><br><br>
                        7. LA LIBERTAD ES UN BIEN MUY PRECIADO
                        <br><br>
                        Si, PHP es totalmente libre y multi-plataforma, por lo que no importa que sistema operativo uses. Su desarrollo es posible en cualquier sitio (siempre y cuando la máquina sea capaz de ejecutar el código).
